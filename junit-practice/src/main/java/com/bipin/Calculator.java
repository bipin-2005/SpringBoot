package com.bipin;
public class Calculator{
        int add(int a,int b){
            return a+b;
        }
        int sub(int a,int b){
            return a-b;
        }
        int mul(int a,int b){
            return a*b;
        }
        int div(int a,int b){
            return a/b;
        }
}
